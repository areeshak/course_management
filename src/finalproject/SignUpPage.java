    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userName = jTextField1.getText().trim();
        String userPassword = String.valueOf(jPasswordField1.getPassword());
        if(!jTextField1.getText().isEmpty() || userPassword.length() != 0)
        {
            int check = u1.find(userName);
            if(check == -1)
            {
                try
                {
                    FileWriter detailsWriter = new FileWriter("UserDetails.txt",true);
                    detailsWriter.write(userName + "," + userPassword + ": " + "\n");         //add user details in text file
                    u1.insertUser(userName, userPassword);
                    
                    detailsWriter.close();
                    new UserPage(userName).setVisible(true);                                 //opens User page if name and password matches
                    this.setVisible(false);                                             //closes sign in page
                }
                catch(Exception e)
                {
                    System.out.println(e);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Account Exists. \n Login to your account", "Error", JOptionPane.ERROR_MESSAGE);
                new SignInPage().setVisible(true);                                 //opens Singn In page if user exists
                this.setVisible(false);      
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Enter credientials", "Error", JOptionPane.ERROR_MESSAGE);
        }
            
        
    }//GEN-LAST:event_jButton1ActionPerformed

