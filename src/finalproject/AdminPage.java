    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here                                     //Insert Course Button

        boolean check = false;
        if(!jTextField3.getText().isEmpty() && !jTextField2.getText().isEmpty())                                       //check if textfiled empty or not
        {
            try                                                                     //check if course already present or not
            {
                FileReader programReader = new FileReader("ProgramsDetails.txt");
                BufferedReader FileInput = new BufferedReader(programReader);
                String line = "";
                while((line = FileInput.readLine()) != null)
                {
                    if(line.toLowerCase().contains(jTextField3.getText().toLowerCase()))
                    {
                        check = true;
                        break;
                    }
                }
                FileInput.close();
                programReader.close();
                JOptionPane.showMessageDialog(null, "Course Added");
            }
            catch(Exception e)
            {
                System.out.println(e);
            }

            if(check != true)
            {
                a1.addCourse(jComboBox2.getSelectedItem().toString(),jTextField3.getText(),Integer.parseInt(jTextField2.getText()),true);       //insert course in admin class
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Course already in system");
            }

            jTextField3.setText("");                                                //clear course name field
            jTextField2.setText("");                                                //clear student capacity field
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Enter details","Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton11ActionPerformed

