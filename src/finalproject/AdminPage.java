    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        try
        {
            FileReader listReader = new FileReader("UserDetails.txt");
            BufferedReader FileInput = new BufferedReader(listReader);
            DefaultListModel dm = new  DefaultListModel();
            programSelected = jComboBox8.getSelectedItem().toString();
            courseSelected = jComboBox9.getSelectedItem().toString();
            String line;
            int index;
            while((line = FileInput.readLine()) != null)
            {
                if(line.contains(programSelected) && line.contains(courseSelected))
                {
                    index = line.indexOf(",");
                    dm.addElement(line.substring(0,index));
                }
            }
            if(!dm.isEmpty())
            {
                 jList2.setModel(dm);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"No student enrolled");
            }
            FileInput.close();
            listReader.close();
        }catch(Exception e)
        {
            
        }
    }//GEN-LAST:event_jButton13ActionPerformed

